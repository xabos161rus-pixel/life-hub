// Платформа определяет формулировки обучения: на таче — «тап/свайп/удержание»,
// с мышью и клавиатурой — «клик/Enter/перетащите». Оценивается один раз при
// старте: гибридные устройства (iPad с трекпадом) считаем тачем — их основной
// ввод пальцем.
export const isTouch: boolean =
  typeof window !== 'undefined' && (window.matchMedia?.('(pointer: coarse)').matches ?? false);
